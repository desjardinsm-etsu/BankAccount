public class Owner
{
    private String firstName;
    private String lastName;

    public void setFirstName(String fN)
    {
        firstName = fN;
    }
    public void setLastName(String lN)
    {
        lastName = lN;
    }

    public String getFirstName()
    {
        return firstName;
    }

    public String getLastName()
    {
        return lastName;
    }

}
